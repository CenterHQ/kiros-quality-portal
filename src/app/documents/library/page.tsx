'use client'

import { useState, useEffect } from 'react'
import { useProfile } from '@/lib/ProfileContext'

interface AIDocument {
  id: string
  title: string
  document_type: string
  topic_folder: string
  sharepoint_folder_path: string | null
  sharepoint_urls: Record<string, string>
  format_variants: string[]
  sync_status: string
  version: number
  created_at: string
  updated_at: string
  last_synced_at: string | null
}

const TYPE_COLORS: Record<string, string> = {
  report: 'bg-blue-100 text-blue-700',
  letter: 'bg-green-100 text-green-700',
  policy: 'bg-red-100 text-red-700',
  plan: 'bg-purple-100 text-purple-700',
  checklist: 'bg-orange-100 text-orange-700',
  agenda: 'bg-yellow-100 text-yellow-700',
  communication: 'bg-pink-100 text-pink-700',
  guide: 'bg-indigo-100 text-indigo-700',
  summary: 'bg-teal-100 text-teal-700',
  other: 'bg-gray-100 text-gray-700',
}

const SYNC_STATUS_LABELS: Record<string, { label: string; color: string }> = {
  synced: { label: 'Synced', color: 'text-green-600' },
  modified_externally: { label: 'Modified in SharePoint', color: 'text-yellow-600' },
  pending_upload: { label: 'Pending Upload', color: 'text-orange-600' },
  upload_failed: { label: 'Upload Failed', color: 'text-red-600' },
  sync_error: { label: 'Sync Error', color: 'text-red-600' },
}

export default function DocumentLibraryPage() {
  const profile = useProfile()
  const [documents, setDocuments] = useState<AIDocument[]>([])
  const [topics, setTopics] = useState<string[]>([])
  const [loading, setLoading] = useState(true)
  const [syncing, setSyncing] = useState(false)
  const [total, setTotal] = useState(0)
  const [filters, setFilters] = useState({ topic: '', type: '', search: '' })
  const [flash, setFlash] = useState<string | null>(null)

  useEffect(() => {
    loadDocuments()
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [filters])

  useEffect(() => {
    if (flash) {
      const t = setTimeout(() => setFlash(null), 4000)
      return () => clearTimeout(t)
    }
  }, [flash])

  async function loadDocuments() {
    setLoading(true)
    const params = new URLSearchParams()
    if (filters.topic) params.set('topic', filters.topic)
    if (filters.type) params.set('type', filters.type)
    if (filters.search) params.set('search', filters.search)

    const res = await fetch(`/api/documents/library?${params}`)
    if (res.ok) {
      const data = await res.json()
      setDocuments(data.documents)
      setTotal(data.total)
      setTopics(data.topics)
    }
    setLoading(false)
  }

  async function handleSync() {
    setSyncing(true)
    try {
      const res = await fetch('/api/documents/resync', { method: 'POST' })
      if (res.ok) {
        const result = await res.json()
        setFlash(`Sync complete: ${result.checked} checked, ${result.updated} updated, ${result.errors} errors`)
        loadDocuments()
      } else {
        setFlash('Sync failed')
      }
    } catch {
      setFlash('Sync failed — network error')
    }
    setSyncing(false)
  }

  if (!profile) {
    return <div className="p-8 text-center text-muted-foreground">Loading...</div>
  }

  return (
    <div className="p-4 sm:p-6 lg:p-8 max-w-7xl mx-auto space-y-6">
      {/* Flash */}
      {flash && (
        <div className="p-3 rounded-lg text-sm bg-blue-50 text-blue-800 border border-blue-200">
          {flash}
        </div>
      )}

      {/* Header */}
      <div className="flex items-center justify-between">
        <div>
          <h1 className="text-2xl font-bold">AI Document Library</h1>
          <p className="text-sm text-muted-foreground mt-1">{total} documents generated by Kiros AI</p>
        </div>
        <button
          onClick={handleSync}
          disabled={syncing}
          className="px-4 py-2 bg-primary text-primary-foreground rounded-lg text-sm font-medium hover:opacity-90 disabled:opacity-50 transition-opacity"
        >
          {syncing ? 'Syncing...' : 'Sync Now'}
        </button>
      </div>

      {/* Filters */}
      <div className="flex flex-wrap gap-3">
        <select
          value={filters.topic}
          onChange={e => setFilters(f => ({ ...f, topic: e.target.value }))}
          className="px-3 py-2 rounded-lg border text-sm"
        >
          <option value="">All Topics</option>
          {topics.map(t => <option key={t} value={t}>{t}</option>)}
        </select>

        <select
          value={filters.type}
          onChange={e => setFilters(f => ({ ...f, type: e.target.value }))}
          className="px-3 py-2 rounded-lg border text-sm"
        >
          <option value="">All Types</option>
          {['report', 'letter', 'policy', 'plan', 'checklist', 'agenda', 'communication', 'guide', 'summary'].map(t => (
            <option key={t} value={t}>{t.charAt(0).toUpperCase() + t.slice(1)}</option>
          ))}
        </select>

        <input
          value={filters.search}
          onChange={e => setFilters(f => ({ ...f, search: e.target.value }))}
          className="px-3 py-2 rounded-lg border text-sm flex-1 min-w-[200px]"
          placeholder="Search documents..."
        />
      </div>

      {/* Document Grid */}
      {loading ? (
        <div className="text-center py-12 text-muted-foreground">Loading documents...</div>
      ) : documents.length === 0 ? (
        <div className="text-center py-12 text-muted-foreground">No documents found. AI-generated documents will appear here.</div>
      ) : (
        <div className="grid gap-4">
          {documents.map(doc => {
            const syncInfo = SYNC_STATUS_LABELS[doc.sync_status] || SYNC_STATUS_LABELS.synced
            return (
              <div key={doc.id} className="p-4 rounded-lg border bg-card hover:shadow-sm transition-shadow">
                <div className="flex items-start justify-between">
                  <div className="flex-1 min-w-0">
                    <div className="flex items-center gap-2 flex-wrap">
                      <h3 className="font-semibold text-sm">{doc.title}</h3>
                      <span className={`px-2 py-0.5 rounded-full text-[10px] font-medium ${TYPE_COLORS[doc.document_type] || TYPE_COLORS.other}`}>
                        {doc.document_type}
                      </span>
                      <span className="px-2 py-0.5 rounded-full text-[10px] font-medium bg-gray-100 text-gray-600">
                        {doc.topic_folder}
                      </span>
                      <span className={`text-[10px] font-medium ${syncInfo.color}`}>
                        {syncInfo.label}
                      </span>
                    </div>
                    <div className="text-xs text-muted-foreground mt-1">
                      Created: {new Date(doc.created_at).toLocaleDateString('en-AU', { day: 'numeric', month: 'short', year: 'numeric', hour: '2-digit', minute: '2-digit' })}
                      {doc.last_synced_at && ` | Last synced: ${new Date(doc.last_synced_at).toLocaleDateString('en-AU', { day: 'numeric', month: 'short', hour: '2-digit', minute: '2-digit' })}`}
                      {doc.version > 1 && ` | v${doc.version}`}
                    </div>
                  </div>

                  {/* Format buttons */}
                  <div className="flex items-center gap-1.5 ml-4 flex-shrink-0">
                    {Object.entries(doc.sharepoint_urls || {}).map(([format, url]) => (
                      <a
                        key={format}
                        href={url}
                        target="_blank"
                        rel="noopener noreferrer"
                        className="px-2 py-1 rounded text-[10px] font-medium bg-purple-50 text-purple-700 border border-purple-200 hover:bg-purple-100 transition-colors uppercase"
                      >
                        {format}
                      </a>
                    ))}
                    {doc.sharepoint_folder_path && (
                      <a
                        href={`https://kirosgroup.sharepoint.com/sites/operations/${doc.sharepoint_folder_path}`}
                        target="_blank"
                        rel="noopener noreferrer"
                        className="px-2 py-1 rounded text-[10px] font-medium bg-blue-50 text-blue-700 border border-blue-200 hover:bg-blue-100 transition-colors"
                      >
                        SharePoint
                      </a>
                    )}
                  </div>
                </div>
              </div>
            )
          })}
        </div>
      )}
    </div>
  )
}
