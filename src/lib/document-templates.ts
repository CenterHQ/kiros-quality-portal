export async function generateExcelDocument(
  title: string,
  content: string,
  options: DocumentOptions
): Promise<Buffer> {
  const dateStr = formatDate(options.date)
  const blocks = parseMarkdownBlocks(content)

  const workbook = new ExcelJS.Workbook()
  workbook.creator = options.author ?? BRAND.centreName
  workbook.created = new Date()

  const sheetName = title.substring(0, 31).replace(/[*?:/\\[\]]/g, '') // Excel 31-char limit
  const sheet = workbook.addWorksheet(sheetName)

  // Header row
  const headerRow = sheet.addRow([title, '', dateStr])
  headerRow.height = 30
  headerRow.eachCell((cell) => {
    cell.font = { bold: true, color: { argb: 'FFFFFFFF' }, size: 14 }
    cell.fill = {
      type: 'pattern',
      pattern: 'solid',
      fgColor: { argb: 'FF470DA8' },
    }
    cell.alignment = { vertical: 'middle' }
  })
  sheet.mergeCells('A1:B1')

  // Meta row
  if (options.recipient || options.author) {
    const metaParts: string[] = []
    if (options.recipient) metaParts.push(`Prepared for: ${options.recipient}`)
    if (options.author) metaParts.push(`Author: ${options.author}`)
    const metaRow = sheet.addRow([metaParts.join('  |  ')])
    metaRow.eachCell((cell) => {
      cell.font = { color: { argb: 'FF666666' }, size: 10, italic: true }
    })
  }

  sheet.addRow([]) // blank separator

  // Body content
  for (const block of blocks) {
    switch (block.type) {
      case 'heading': {
        const row = sheet.addRow([stripInlineMarkdown(block.text)])
        row.eachCell((cell) => {
          cell.font = {
            bold: true,
            size: block.level === 1 ? 14 : block.level === 2 ? 12 : 11,
            color: { argb: 'FF470DA8' },
          }
        })
        break
      }

      case 'table': {
        if (block.rows && block.rows.length > 0) {
          block.rows.forEach((rowData, ri) => {
            const row = sheet.addRow(rowData.map(stripInlineMarkdown))
            if (ri === 0) {
              row.eachCell((cell) => {
                cell.font = { bold: true, color: { argb: 'FFFFFFFF' }, size: 10 }
                cell.fill = {
                  type: 'pattern',
                  pattern: 'solid',
                  fgColor: { argb: 'FF470DA8' },
                }
                cell.border = {
                  bottom: { style: 'thin', color: { argb: 'FF470DA8' } },
                }
              })
            } else {
              row.eachCell((cell) => {
                cell.border = {
                  bottom: { style: 'thin', color: { argb: 'FFDDDDDD' } },
                }
              })
            }
          })
        }
        break
      }

      default: {
        const text = stripInlineMarkdown(block.text)
        if (text.trim()) {
          sheet.addRow([text])
        }
        break
      }
    }
  }

  // Auto-width columns
  sheet.columns.forEach((column) => {
    let maxLength = 10
    column.eachCell?.({ includeEmpty: false }, (cell) => {
      const cellLength = cell.value ? String(cell.value).length : 0
      if (cellLength > maxLength) maxLength = cellLength
    })
    column.width = Math.min(maxLength + 4, 80)
  })

  // Footer row
  sheet.addRow([])
  const footerRow = sheet.addRow([brandFooter()])
  footerRow.eachCell((cell) => {
    cell.font = { size: 8, color: { argb: 'FF666666' }, italic: true }
  })

  const arrayBuffer = await workbook.xlsx.writeBuffer()
  return Buffer.from(arrayBuffer)
}

