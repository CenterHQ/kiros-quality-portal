function markdownToDocxParagraphs(blocks: MarkdownBlock[]): Paragraph[] {
  const paragraphs: Paragraph[] = []

  for (const block of blocks) {
    switch (block.type) {
      case 'heading': {
        const level =
          block.level === 1
            ? HeadingLevel.HEADING_1
            : block.level === 2
              ? HeadingLevel.HEADING_2
              : HeadingLevel.HEADING_3
        const headingSize = block.level === 3 ? 22 : 32 // half-points: 11pt or 16pt
        paragraphs.push(
          new Paragraph({
            heading: level,
            children: parseInlineRunsStyled(block.text, { bold: true, color: '470DA8', size: headingSize }),
            spacing: { before: 240, after: 120 },
          })
        )
        break
      }

      case 'paragraph':
        paragraphs.push(
          new Paragraph({
            children: parseInlineRuns(block.text),
            spacing: { after: 120 },
          })
        )
        break

      case 'list-item':
        paragraphs.push(
          new Paragraph({
            children: parseInlineRuns(block.text),
            spacing: { after: 60 },
            indent: { left: 360 },
          })
        )
        break

      case 'blockquote':
        paragraphs.push(
          new Paragraph({
            children: [
              new TextRun({
                text: stripInlineMarkdown(block.text),
                italics: true,
                color: '666666',
                size: 20, // 10pt
              }),
            ],
            spacing: { after: 120 },
            indent: { left: 480 },
            border: {
              left: {
                color: '470DA8',
                space: 8,
                style: BorderStyle.SINGLE,
                size: 6,
              },
            },
          })
        )
        break

      case 'code':
        paragraphs.push(
          new Paragraph({
            children: [
              new TextRun({
                text: block.text,
                font: 'Courier New',
                size: 18,
              }),
            ],
            spacing: { after: 120 },
            shading: { fill: 'f5f5f5' },
          })
        )
        break

      case 'table': {
        if (block.rows && block.rows.length > 0) {
          const tableRows = block.rows.map((row, ri) =>
            new TableRow({
              children: row.map(
                (cell) =>
                  new TableCell({
                    children: [
                      new Paragraph({
                        children: [
                          new TextRun({
                            text: stripInlineMarkdown(cell),
                            bold: ri === 0,
                            color: ri === 0 ? 'ffffff' : '1a1a1a',
                            size: 20,
                          }),
                        ],
                      }),
                    ],
                    shading: ri === 0 ? { fill: '470DA8' } : undefined,
                  })
              ),
            })
          )

          paragraphs.push(
            new Paragraph({ spacing: { before: 120 } })
          )
          // We cannot push a Table into Paragraph[], so we use a workaround:
          // docx library allows Table in the document sections children.
          // We'll tag it onto the paragraphs array — the caller must handle it.
          ;(paragraphs as unknown as (Paragraph | Table)[]).push(
            new Table({
              rows: tableRows,
              width: { size: 100, type: WidthType.PERCENTAGE },
            })
          )
          paragraphs.push(
            new Paragraph({ spacing: { after: 120 } })
          )
        }
        break
      }
    }
  }

  return paragraphs
}

/** Parse inline markdown (bold, italic) into TextRun[] */
function parseInlineRuns(text: string): TextRun[] {
  const runs: TextRun[] = []
  // Split on bold (**text** or __text__) patterns
  const parts = text.split(/(\*\*[^*]+\*\*|__[^_]+__)/g)
  for (const part of parts) {
    const boldMatch = part.match(/^\*\*(.+)\*\*$/) || part.match(/^__(.+)__$/)
    if (boldMatch) {
      runs.push(new TextRun({ text: stripInlineMarkdown(boldMatch[1]), bold: true }))
    } else if (part.length > 0) {
      runs.push(new TextRun({ text: stripInlineMarkdown(part) }))
    }
  }
  return runs
}

/** Parse inline markdown with additional style overrides applied to all runs */
function parseInlineRunsStyled(text: string, style: { bold?: boolean; color?: string; size?: number }): TextRun[] {
  const runs: TextRun[] = []
  const parts = text.split(/(\*\*[^*]+\*\*|__[^_]+__)/g)
  for (const part of parts) {
    const boldMatch = part.match(/^\*\*(.+)\*\*$/) || part.match(/^__(.+)__$/)
    if (boldMatch) {
      runs.push(new TextRun({ text: stripInlineMarkdown(boldMatch[1]), bold: true, ...style }))
    } else if (part.length > 0) {
      runs.push(new TextRun({ text: stripInlineMarkdown(part), ...style }))
    }
  }
  return runs
}

export async function generateWordDocument(
  title: string,
  content: string,
  options: DocumentOptions
): Promise<Buffer> {
  const dateStr = formatDate(options.date)
  const blocks = parseMarkdownBlocks(content)
  const bodyChildren = markdownToDocxParagraphs(blocks)

  const doc = new DocxDocument({
    creator: options.author ?? BRAND.centreName,
    title,
    description: `${title} - ${BRAND.centreName}`,
    sections: [
      {
        headers: {
          default: new Header({
            children: [
              new Paragraph({
                children: [
                  new TextRun({
                    text: BRAND.centreNameUpper,
                    bold: true,
                    color: '470DA8',
                    size: 28, // 14pt
                  }),
                  new TextRun({
                    text: '    |    ',
                    color: 'EDC430',
                    size: 20, // 10pt
                  }),
                  new TextRun({
                    text: title,
                    color: '470DA8',
                    size: 22, // 11pt
                  }),
                ],
              }),
            ],
          }),
        },
        footers: {
          default: new Footer({
            children: [
              new Paragraph({
                alignment: AlignmentType.CENTER,
                children: [
                  new TextRun({
                    text: brandFooter(),
                    size: 16,
                    color: '666666',
                  }),
                  new TextRun({
                    text: '    |    Page ',
                    size: 16,
                    color: '666666',
                  }),
                  new TextRun({
                    children: [PageNumber.CURRENT],
                    size: 16,
                    color: '666666',
                  }),
                  new TextRun({
                    text: ' of ',
                    size: 16,
                    color: '666666',
                  }),
                  new TextRun({
                    children: [PageNumber.TOTAL_PAGES],
                    size: 16,
                    color: '666666',
                  }),
                ],
              }),
            ],
          }),
        },
        properties: {
          page: {
            size: { width: 11906, height: 16838 }, // A4
            margin: { top: 1440, right: 1080, bottom: 1440, left: 1080 },
            pageNumbers: { start: 1 },
          },
        },
        children: [
          // Title block — centred: brand name, document title, date in gold
          new Paragraph({
            alignment: AlignmentType.CENTER,
            children: [
              new TextRun({
                text: BRAND.centreNameUpper,
                bold: true,
                color: '470DA8',
                size: 44, // 22pt
              }),
            ],
            spacing: { after: 60 },
          }),
          new Paragraph({
            alignment: AlignmentType.CENTER,
            children: [
              new TextRun({
                text: title,
                color: '470DA8',
                size: 32, // 16pt
              }),
            ],
            spacing: { after: 60 },
          }),
          new Paragraph({
            alignment: AlignmentType.CENTER,
            children: [
              new TextRun({
                text: dateStr,
                bold: true,
                color: 'EDC430',
                size: 26, // 13pt
              }),
            ],
            spacing: { after: 120 },
          }),
          // Meta info
          ...(options.recipient
            ? [
                new Paragraph({
                  alignment: AlignmentType.CENTER,
                  children: [
                    new TextRun({
                      text: `Prepared for: ${options.recipient}`,
                      color: '666666',
                      size: 20,
                    }),
                  ],
                }),
              ]
            : []),
          ...(options.author
            ? [
                new Paragraph({
                  alignment: AlignmentType.CENTER,
                  children: [
                    new TextRun({
                      text: `Author: ${options.author}`,
                      color: '666666',
                      size: 20,
                    }),
                  ],
                }),
              ]
            : []),
          new Paragraph({ spacing: { after: 200 } }),
          // Body
          ...(bodyChildren as Paragraph[]),
        ],
      },
    ],
  })

  const buffer = await Packer.toBuffer(doc)
  return Buffer.from(buffer)
}

