const md = new MarkdownIt({ html: false, linkify: true, typographer: true })

interface MarkdownBlock {
  type:
    | 'heading'
    | 'paragraph'
    | 'list-item'
    | 'table'
    | 'blockquote'
    | 'code'
  level?: number // heading level 1-6
  text: string
  bold?: boolean
  rows?: string[][] // for tables
}

/**
 * Very lightweight markdown-to-block parser. We lean on markdown-it to
 * tokenise, then walk the token stream to produce simple block descriptors
 * that each export backend can render natively.
 */
function parseMarkdownBlocks(markdown: string): MarkdownBlock[] {
  const tokens = md.parse(markdown, {})
  const blocks: MarkdownBlock[] = []
  let i = 0

  while (i < tokens.length) {
    const token = tokens[i]

    if (token.type === 'heading_open') {
      const level = parseInt(token.tag.replace('h', ''), 10)
      const inline = tokens[i + 1]
      blocks.push({
        type: 'heading',
        level,
        text: inline?.content ?? '',
      })
      i += 3 // heading_open, inline, heading_close
      continue
    }

    if (token.type === 'paragraph_open') {
      const inline = tokens[i + 1]
      blocks.push({
        type: 'paragraph',
        text: inline?.content ?? '',
      })
      i += 3
      continue
    }

    if (token.type === 'bullet_list_open' || token.type === 'ordered_list_open') {
      i += 1
      let itemIndex = 1
      while (i < tokens.length && tokens[i].type !== 'bullet_list_close' && tokens[i].type !== 'ordered_list_close') {
        if (tokens[i].type === 'list_item_open') {
          i += 1
          // list items can contain paragraph_open/inline/paragraph_close or just inline
          if (i < tokens.length && tokens[i].type === 'paragraph_open') {
            const inline = tokens[i + 1]
            const prefix = token.type === 'ordered_list_open' ? `${itemIndex}. ` : '\u2022 '
            blocks.push({
              type: 'list-item',
              text: prefix + (inline?.content ?? ''),
            })
            itemIndex++
            i += 3 // paragraph_open, inline, paragraph_close
          }
        } else {
          i += 1
        }
      }
      i += 1 // skip list close
      continue
    }

    if (token.type === 'table_open') {
      const rows: string[][] = []
      i += 1
      while (i < tokens.length && tokens[i].type !== 'table_close') {
        if (tokens[i].type === 'tr_open') {
          const row: string[] = []
          i += 1
          while (i < tokens.length && tokens[i].type !== 'tr_close') {
            if (tokens[i].type === 'th_open' || tokens[i].type === 'td_open') {
              const inline = tokens[i + 1]
              row.push(inline?.content ?? '')
              i += 3
            } else {
              i += 1
            }
          }
          rows.push(row)
        }
        i += 1
      }
      if (rows.length > 0) {
        blocks.push({ type: 'table', text: '', rows })
      }
      i += 1
      continue
    }

    if (token.type === 'blockquote_open') {
      i += 1
      let quoteText = ''
      while (i < tokens.length && tokens[i].type !== 'blockquote_close') {
        if (tokens[i].type === 'paragraph_open') {
          const inline = tokens[i + 1]
          quoteText += (inline?.content ?? '') + '\n'
          i += 3
        } else {
          i += 1
        }
      }
      blocks.push({ type: 'blockquote', text: quoteText.trim() })
      i += 1
      continue
    }

    if (token.type === 'fence' || token.type === 'code_block') {
      blocks.push({ type: 'code', text: token.content })
      i += 1
      continue
    }

    i += 1
  }

  return blocks
}

/** Strip simple inline markdown formatting for plain-text contexts */
function stripInlineMarkdown(text: string): string {
  return text
    .replace(/\*\*(.+?)\*\*/g, '$1')
    .replace(/\*(.+?)\*/g, '$1')
    .replace(/__(.+?)__/g, '$1')
    .replace(/_(.+?)_/g, '$1')
    .replace(/`(.+?)`/g, '$1')
    .replace(/\[(.+?)\]\(.+?\)/g, '$1')
}

/** Check if inline text contains bold markers */
function hasBold(text: string): boolean {
  return /\*\*(.+?)\*\*/.test(text) || /__(.+?)__/.test(text)
}

function formatDate(date?: string): string {
  if (date) return date
  return new Date().toLocaleDateString('en-AU', {
    day: 'numeric',
    month: 'long',
    year: 'numeric',
  })
}

