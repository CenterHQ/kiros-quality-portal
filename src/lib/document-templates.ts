function renderPdfBlock(block: MarkdownBlock, index: number): React.ReactElement {
  const key = `block-${index}`

  switch (block.type) {
    case 'heading': {
      const style =
        block.level === 1
          ? pdfStyles.heading1
          : block.level === 2
            ? pdfStyles.heading2
            : pdfStyles.heading3
      return React.createElement(Text, { key, style }, stripInlineMarkdown(block.text))
    }

    case 'paragraph': {
      const plain = stripInlineMarkdown(block.text)
      if (hasBold(block.text)) {
        // Render with bold segments
        const parts = block.text.split(/(\*\*[^*]+\*\*|__[^_]+__)/g)
        const children = parts.map((part, pi) => {
          const boldMatch = part.match(/^\*\*(.+)\*\*$/) || part.match(/^__(.+)__$/)
          if (boldMatch) {
            return React.createElement(
              Text,
              { key: `${key}-p-${pi}`, style: pdfStyles.boldText },
              boldMatch[1]
            )
          }
          return stripInlineMarkdown(part)
        })
        return React.createElement(Text, { key, style: pdfStyles.paragraph }, ...children)
      }
      return React.createElement(Text, { key, style: pdfStyles.paragraph }, plain)
    }

    case 'list-item':
      return React.createElement(
        Text,
        { key, style: pdfStyles.listItem },
        stripInlineMarkdown(block.text)
      )

    case 'blockquote':
      return React.createElement(
        View,
        { key, style: pdfStyles.blockquote },
        React.createElement(Text, null, stripInlineMarkdown(block.text))
      )

    case 'code':
      return React.createElement(Text, { key, style: pdfStyles.codeBlock }, block.text)

    case 'table': {
      if (!block.rows || block.rows.length === 0) {
        return React.createElement(View, { key })
      }
      const [headerRow, ...dataRows] = block.rows
      const tableChildren: React.ReactElement[] = []

      // Header
      tableChildren.push(
        React.createElement(
          View,
          { key: `${key}-th`, style: pdfStyles.tableHeaderRow },
          ...headerRow.map((cell, ci) =>
            React.createElement(
              Text,
              { key: `${key}-th-${ci}`, style: pdfStyles.tableHeaderCell },
              stripInlineMarkdown(cell)
            )
          )
        )
      )

      // Data rows
      dataRows.forEach((row, ri) => {
        tableChildren.push(
          React.createElement(
            View,
            { key: `${key}-tr-${ri}`, style: pdfStyles.tableRow },
            ...row.map((cell, ci) =>
              React.createElement(
                Text,
                { key: `${key}-tr-${ri}-${ci}`, style: pdfStyles.tableCell },
                stripInlineMarkdown(cell)
              )
            )
          )
        )
      })

      return React.createElement(
        View,
        { key, style: { marginBottom: 10 } },
        ...tableChildren
      )
    }

    default:
      return React.createElement(View, { key })
  }
}

export async function generatePdfDocument(
  title: string,
  content: string,
  options: DocumentOptions
): Promise<Buffer> {
  const dateStr = formatDate(options.date)
  const blocks = parseMarkdownBlocks(content)

  const headerView = React.createElement(
    View,
    { style: pdfStyles.header, fixed: true },
    React.createElement(Text, { style: pdfStyles.headerText }, BRAND.centreNameUpper),
    React.createElement(Text, { style: pdfStyles.headerSeparator }, '|'),
    React.createElement(Text, { style: pdfStyles.headerTitle }, title)
  )

  const titleSection = React.createElement(
    View,
    { style: pdfStyles.titleSection },
    React.createElement(Text, { style: pdfStyles.title }, BRAND.centreNameUpper),
    React.createElement(Text, { style: pdfStyles.titleSubtitle }, title),
    React.createElement(Text, { style: pdfStyles.titleDate }, dateStr),
    options.recipient
      ? React.createElement(Text, { style: pdfStyles.subtitle }, `Prepared for: ${options.recipient}`)
      : null,
    options.author
      ? React.createElement(Text, { style: pdfStyles.subtitle }, `Author: ${options.author}`)
      : null
  )

  const footerView = React.createElement(
    View,
    { style: pdfStyles.footer, fixed: true },
    React.createElement(
      Text,
      { style: pdfStyles.footerText, render: ({ pageNumber, totalPages }: { pageNumber: number; totalPages: number }) => `${brandFooter()}    |    Page ${pageNumber} of ${totalPages}` },
    )
  )

  const bodyBlocks = blocks.map((block, i) => renderPdfBlock(block, i))

  const page = React.createElement(
    Page,
    { size: 'A4', style: pdfStyles.page },
    headerView,
    titleSection,
    ...bodyBlocks,
    footerView
  )

  const doc = React.createElement(Document, {
    title,
    author: options.author ?? BRAND.centreName,
    creator: BRAND.tagline,
  }, page)

  const buffer = await renderToBuffer(doc)
  return Buffer.from(buffer)
}

