// ============================================================
// Helper: parse config value to typed value
// ============================================================

function parseValue(value: string, type: string): string | number | boolean | string[] {
  switch (type) {
    case 'int': return parseInt(value, 10) || 0
    case 'float': return parseFloat(value) || 0
    case 'bool': return value === 'true'
    case 'json': try { return JSON.parse(value) } catch { return value }
    default: return value
  }
}

function hexToRgb(hex: string): { r: number; g: number; b: number } {
  const result = /^#?([a-f\d]{2})([a-f\d]{2})([a-f\d]{2})$/i.exec(hex)
  return result ? { r: parseInt(result[1], 16), g: parseInt(result[2], 16), b: parseInt(result[3], 16) } : { r: 0, g: 0, b: 0 }
}

