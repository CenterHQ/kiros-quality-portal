// ============================================================
// Tool permissions loader
// ============================================================

export async function loadToolPermissions(supabase: SupabaseClient, toolType: 'main' | 'marketing' = 'main'): Promise<Map<string, string[]>> {
  try {
    const { data, error } = await supabase
      .from('ai_tool_permissions')
      .select('tool_name, allowed_roles')
      .eq('tool_type', toolType)
      .eq('is_active', true)
    if (error || !data) return new Map()
    return new Map(data.map((r: { tool_name: string; allowed_roles: string[] }) => [r.tool_name, r.allowed_roles]))
  } catch {
    return new Map()
  }
}

// ============================================================
// Document styles loader
// ============================================================

export async function loadDocumentStyles(supabase: SupabaseClient, format: string): Promise<Record<string, unknown>> {
  try {
    const { data, error } = await supabase
      .from('ai_document_styles')
      .select('styles')
      .eq('format', format)
      .single()
    if (error || !data) return {}
    return data.styles as Record<string, unknown>
  } catch {
    return {}
  }
}

